void Delay_us( u8 time_us ) {
  register u8 i;
  register u8 j;
  for( i=0;i<time_us;i++ ) {
    for( j=0;j<5;j++ ) {  // 25CLK
    
      asm("nop");       //1CLK         
      asm("nop");       //1CLK         
      asm("nop");       //1CLK         
      asm("nop");       //1CLK         
      asm("nop");       //1CLK                  
    }      
  } // 25CLK*0.04us=1us
}

void Delay_ms( u16 time_ms ) {
  register u16 i;
  for( i=0;i<time_ms;i++ ) {
    Delay_us(250);
    Delay_us(250);
    Delay_us(250);
    Delay_us(250);
  }
}

void GLCD_Configuration(void) {
  GPIO_InitTypeDef GPIO_InitStructure;
  
  /* RCC configuration -------------------------------------------------------*/
  /* Enable GLCD pin */
  RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOA | RCC_AHB1Periph_GPIOC |
                         RCC_AHB1Periph_GPIOD, ENABLE);
  
  /* GPIO configuration ------------------------------------------------------*/
  /* RS, R/W, E, D0, D1 pin configuration */
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_11 | GPIO_Pin_12 | GPIO_Pin_13 | 
                                GPIO_Pin_14 | GPIO_Pin_15;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
  GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_DOWN;
  GPIO_Init(GPIOD, &GPIO_InitStructure);
  
  /* D2, D3, D4, D5 pin configuration */
  GPIO_InitStructure.GPIO_Pin =  GPIO_Pin_6 | GPIO_Pin_7 | GPIO_Pin_8 | GPIO_Pin_9;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
  GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_DOWN;
  GPIO_Init(GPIOC, &GPIO_InitStructure);
  
  /* D6, D7, CS1, CS2, RESET pin configuration */
  GPIO_InitStructure.GPIO_Pin =  GPIO_Pin_8 | GPIO_Pin_9 | GPIO_Pin_10 | GPIO_Pin_11 | GPIO_Pin_12;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;
  GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_DOWN;
  GPIO_Init(GPIOA, &GPIO_InitStructure);
  
  // take 1 us to reset GLCD
  GLCD_RESET_LOW();
  Delay_us(1);
  GLCD_RESET_HIGH();
  
  // initial work to be done at least once at boot
  GLCD_Handler(RS_READ, CS12, DISPLAY_ON);  // display on
  GLCD_Handler(RS_READ, CS12, START_LINE);  // locate start line
  
  /* Clear GLCD screen */
  GLCD_Clear();
  
  /* GLCD Introduction */
  GLCD_Intro();
  
  /* GLCD Basic Interface */
  //GLCD_BasicIF();
  
  GLCD_CustomIF();
}

void GLCD_Handler(char rs, char cs, char data) {
  Delay_us(25);
  GLCD_ENABLE_LOW();
  Delay_us(1);
  GLCD_RW_LOW();
  Delay_us(1);
  
  switch(rs) {
  case RS_WRITE : GLCD_RS_HIGH(); break;
  case RS_READ : GLCD_RS_LOW(); break;
  default: Delay_us(1); break;
  }
  
  switch(cs) {
  case CS1: GLCD_CS2_LOW(); GLCD_CS1_HIGH(); break;
  case CS2: GLCD_CS1_LOW(); GLCD_CS2_HIGH(); break;
  case CS12: GLCD_CS1_HIGH(); GLCD_CS2_HIGH(); break;
  default: Delay_us(1); break;
  }
  
  GLCD_ENABLE_HIGH();  // E Rise
  Delay_us(1);
  
  GPIO_WriteBit(GPIOD, GPIO_Pin_14, (data & 0x01) == Bit_SET ? Bit_SET : Bit_RESET);  // D0
  GPIO_WriteBit(GPIOD, GPIO_Pin_15, ((data & 0x02) >> 1) == Bit_SET ? Bit_SET : Bit_RESET);  // D1
  GPIO_WriteBit(GPIOC, GPIO_Pin_6, ((data & 0x04) >> 2) == Bit_SET ? Bit_SET : Bit_RESET);  // D2
  GPIO_WriteBit(GPIOC, GPIO_Pin_7, ((data & 0x08) >> 3) == Bit_SET ? Bit_SET : Bit_RESET);  // D3
  GPIO_WriteBit(GPIOC, GPIO_Pin_8, ((data & 0x10) >> 4) == Bit_SET ? Bit_SET : Bit_RESET);  // D4
  GPIO_WriteBit(GPIOC, GPIO_Pin_9, ((data & 0x20) >> 5) == Bit_SET ? Bit_SET : Bit_RESET);  // D5
  GPIO_WriteBit(GPIOA, GPIO_Pin_8, ((data & 0x40) >> 6) == Bit_SET ? Bit_SET : Bit_RESET);  // D6
  GPIO_WriteBit(GPIOA, GPIO_Pin_9, ((data & 0x80) >> 7) == Bit_SET ? Bit_SET : Bit_RESET);  // D7
  Delay_us(1);
  
  GLCD_ENABLE_LOW();
  Delay_us(1);
  GLCD_RW_HIGH();
}

