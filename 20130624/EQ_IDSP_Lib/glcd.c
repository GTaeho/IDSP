void SelectAxisX(int index) {
  int i, j, arrIdx;
  arrIdx = index;
  
  /* Axis Menu */
  for(i = 0; i < 8; i++) {
    GLCD_Handler(RS_READ, CS1, PAGE_SET | i); // page set
    GLCD_Handler(RS_READ, CS1, ADDR_SET);     // address set
    for(j = 0; j < 9; j++) {
      GLCD_Handler(RS_WRITE, CS1, menu_x[8*(j+1)-i-1]);  // write data
    }
  }
  
  for(j=0; j<99; j++) {
    if((arrIdx - j) < 0) {
      current = 99 - (j - arrIdx - 1);
    } else {
      current = arrIdx - j;
    }
    
    /* generate waveform */
    tmp = mAxisBuf.tmp_data_x_lcd[current];
    
    if(j > 54) {
      CS = CS2;
      yadd_off = 0x40;
    } else {
      CS = CS1;
      yadd_off = 0x49;
    }
    
    if(tmp & 0x2000) {
      tmp = (~tmp+1) & 0xFFF;
      sign = 1;
    } else {
      tmp &= 0xFFF;
      sign = 0;
    }
  
    disp = tmp >> 7;
    
    if(sign == 0) {
      
      GLCD_Handler(RS_READ, CS, 0xB8 + 3 - (disp >> 3));
      GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
      GLCD_Handler(RS_WRITE, CS, 0xFF << (7 - (disp % 8)) & 0xFF);
      
      // fill from center to x data
      for(i=0xBB; i > 0xB8 + (3 - (disp >> 3)); i--) {  // 0xBB : Center of the screen
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        GLCD_Handler(RS_WRITE, CS, 0xFF);
      }
      
      // clear from top to x data
      for(i=0xB8; i<0xB8 + (3 - (disp >> 3)); i++) {
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        if(i == 0xB8) {
          GLCD_Handler(RS_WRITE, CS, 0x01);
        } else {
          GLCD_Handler(RS_WRITE, CS, 0); 
        }
      }
      
      // clear from center to bottom
      for(i=0xBC; i < 0xC0; i++) {
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        if(i == 0xBF) {
          GLCD_Handler(RS_WRITE, CS, 0x80);
        } else {
          GLCD_Handler(RS_WRITE, CS, 0); 
        }
      }
      
    } else {
      
      GLCD_Handler(RS_READ, CS, 0xBC + (disp >> 3));
      GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
      GLCD_Handler(RS_WRITE, CS, 0xFF >> (7 - (disp % 8)));
      
      // fill from center to x data in negative direction
      for(i=0xBC; i<0xBC + (disp >> 3); i++) {
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        GLCD_Handler(RS_WRITE, CS, 0xFF);
      }
      
      // clear from bottom to x data
      for(i=0xBF; i> 0xBC + (disp >> 3); i--) {
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        if(i == 0xBF) {
          GLCD_Handler(RS_WRITE, CS, 0x80);
        } else {
          GLCD_Handler(RS_WRITE, CS, 0); 
        }
      }
      
      // clear from top to center
      for(i=0xB8; i<0xBC; i++) {
        GLCD_Handler(RS_READ, CS, i);
        GLCD_Handler(RS_READ, CS, yadd_off + (j % 55));
        if(i == 0xB8) {
          GLCD_Handler(RS_WRITE, CS, 0x01);
        } else {
          GLCD_Handler(RS_WRITE, CS, 0); 
        }
      }
    }
  }
}

